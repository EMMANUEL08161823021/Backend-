// STRICT EQUALITY

console.log(`1: ${1 == '1'}`)
console.log(`2: ${1 === '1'}`)
console.log(`3: ${0 === ''}`)
console.log(`4: ${0 == ''}`)
console.log(`5: ${null == null}`)
console.log(`6: ${null === null}`)
console.log(`7: ${null === undefined}`)
console.log(`8: ${null == undefined}`)
console.log(`9: ${0 == false}`)
console.log(`10: ${0 === false}`)
console.log(`11: ${1 !== '1'}`)
console.log(`11: ${1 != '1'}`)
